--ANIMAL_INS -> DATETIME 보호시작일
--ANIMAL_OUTS  ->  DATETIME 입양일
--SELECT * FROM ANIMAL_INS ;
SELECT
	*
FROM
	(
	SELECT
		O.NAME,
		O.DATETIME
	FROM
		ANIMAL_INS O
	LEFT JOIN ANIMAL_OUTS I
ON
		O.ANIMAL_ID = I.ANIMAL_ID
	WHERE
		I.ANIMAL_ID IS NULL
	ORDER BY
		I.DATETIME
)
WHERE
	ROWNUM <= 3;
